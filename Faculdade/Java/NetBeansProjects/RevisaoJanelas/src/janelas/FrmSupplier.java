    private void BtSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtSaveActionPerformed
        if(TfId.getText().equals("") || TfCnpj.getText().equals("") || TfSocialReason.getText().equals("") ||
            TfFantasyName.getText().equals("") || TfEmail.getText().equals("") || TfPhone.getText().equals("")|| TfContact.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos.");
        }else {
            Supplier Supplier = new Supplier();
            Supplier.setId(Integer.parseInt(TfId.getText()));
            Supplier.setCnpj(TfCnpj.getText());
            Supplier.setSocialReason(TfSocialReason.getText());
            Supplier.setFantasyName(TfFantasyName.getText());
            Supplier.setEmail(TfEmail.getText());
            Supplier.setPhone(TfPhone.getText());
            Supplier.setContact(TfContact.getText());
            JOptionPane.showMessageDialog(null, "Cadastro realizado. ");
        }
    }//GEN-LAST:event_BtSaveActionPerformed

