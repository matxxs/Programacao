    private void BtSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtSaveActionPerformed
        if(TfId.getText().equals("") || TfName.getText().equals("") || TfPhone.getText().equals("") ||
                TfEmail.getText().equals("") || TfCpf.getText().equals("") || TfAddress.getText().equals("")){
                        JOptionPane.showMessageDialog(null, "Preencha todos os campos."); 
        }else {
            Client Client = new Client();
            Client.setId(Integer.parseInt(TfId.getText()));
            Client.setName(TfName.getText());
            Client.setPhone(TfPhone.getText());
            Client.setCpf(TfCpf.getText());
            Client.setAddress(TfAddress.getText());
            JOptionPane.showMessageDialog(null, "Cadastro realizado. ");
        }
    }//GEN-LAST:event_BtSaveActionPerformed

